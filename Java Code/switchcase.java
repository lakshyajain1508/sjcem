package EU1242066;

public class switchcase {

	public static void main(String[] args) {
		
		int day = 5;
		switch(day) {
		case 2:
		System.out.println("Tuesday");
		break;
		case 4:
		System.out.println("Thursday");
		break;
		default: 
    	System.out.println("looking for weekends"); 
    	break;
			
		
		}

	}

}
